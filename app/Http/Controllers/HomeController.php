        $data = Student::create($data);
        
        return redirect()->route('success',[
            'id' => $data->id
        ]);
    }

    public function cetak_pdf($id){

        $student = Student::findOrFail($id);

        $pdf = PDF::loadview('pages.admin.pdf',['student'=>$student]);
